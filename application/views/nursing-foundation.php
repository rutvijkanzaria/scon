							<h3><strong>Innovative Teaching  Strategies</strong></h3>
							<p class="margin10">In addition to the traditional teaching methods we have introduced 
							</p>
							<ol>
								<li>MILA (Multi interactive Learning Algorithm) sessions - consisting of 20 minutes lecture class followed by 20 minutes of self learning activities by the students, based on the following 9 strategies.
									<ul class="bullet_list margin10">
						                <li>SCALE UP-Student centric activity learning environment Small Groups- Activity focused on students specific issues</li>
						                <li>POGIL- Process Oriented Guided Inquiry Learning  (Guiding the search for solutions to questions)</li>
						                <li>CONCEPT MAPPING- (Group session to interrelate concepts)</li>
						                <li>JIGSAW – (Small group discussion + shuffling and new groups task completion)</li>
						                <li>Game Based Learning   (Interactive games to teach specific concepts)</li>
						                <li>Critical Pedagogy (Critique and detect errors in faulty reports)</li>
						                <li>Peer-Led Team Based Learning – Good student is nominated to lead a team to complete learning objectives.</li>
						                <li>Role Play – Enact multi stage interaction based concepts</li>
						                <li>Flipped Class -  ( Students view a previously recorded lecture and report to class to discuss)</li>
						            </ul>
								</li>
								<li>Self Learning Module – Prepared learning material readied by the Eminent Faculty. Overview is delivered by the mentor. Students are divided into small groups and they are issued subject materials, time is given to study the material and learning takes place through discussion among the group.</li>

								<li>Web forum discussion – A problem based/ case based scenario is posted by the mentor through web portal which stimulates student critical thinking and problem solving skills and through active discussion 24/7 learning takes place.  This discussion provides for reviewing clinical performance in debriefing sessions, an essential part of learning.</li>

								<li>Simulation Corner - Students participate in realistic scenarios that provide hands-on experience using real equipment and newly learned skills in a simulated practice setting. </li>

								<li>Self learning desk – The equipment and steps of procedure are placed in the laboratory 24/7 which are utilized by students as and when students are ready to master their skills.</li>

								<li>Quantum assessment  is a formative assessment which is unique in Saveetha College of Nursing and not practiced in any other nursing colleges.</li>

								<li>OSPE- Objective structured practical examination is being used during the practical examination. Here the student is made to perform various skills in different clinical stations.</li>
