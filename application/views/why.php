		 <h3 class="top30 bottom20">Innovative Teaching Methods</h3>
		 
        <p class="bottom25"></p>
		
		 <h3 class="top30 bottom20">Small Group Learning</h3>
		 <img src="<?php echo base_url() ?>images/why/smallgrouplearning.jpg" alt="Course" class=" border_radius img-responsive bottom15">
		 
        <p class="bottom25">The students are divided into group and each group is guided by the facilitator to ensure group progress. Small group learning allows students to develop problem solving, interpersonal, presentational and communication skills, all beneficial to life outside the classroom. These generic skills are difficult to develop in isolation and require feedback and interaction with other individuals</p>
		
		 <h3 class="top30 bottom20">Context Based Learning</h3>
		 
		 <img src="<?php echo base_url() ?>images/why/CBLcommunity.jpg" alt="Course" class=" border_radius img-responsive bottom15">
		 
        <p class="bottom25">The new approach of teaching and learning which was not introduced in our Nursing colleges is Context-based learning (CBL) in Community Health Nursing. It refers to the use of real-life and fictitious examples in “Teaching" environments in order to learn through the actual, practical experience with a subject rather than just its mere. The principles of CBL are that the starting point for learning should be a problem/situation and be student-centred with emphasis on self-directed learning. Students work in groups on real-life nursing situations. The group identifies and researches concepts and principles needed to work as a practicing nurse. The clinical scenarios encourage research, critical thinking, and developing lifelong learning skills.</p>
		
		 <h3 class="top30 bottom20">Multi Interactive Learning Algorithm (MILA)</h3>
		 
		 <img src="<?php echo base_url() ?>images/why/MILAPhotos.jpg" alt="Course" class=" border_radius img-responsive bottom15">
        <p class="bottom25">Introduced Multi Interactive Learning Algorithm (MILA) for B.Sc(N) I year students in Nursing Foundation Subject. This method of teaching This method consists of Twenty minutes of teaching time followed by a Twenty minute activity time to better understand and learn the concept. Activities such as jigsaw, POGIL(Process Oriented Guided Inquiry Learning), mind mapping, game based learning, critical pedagogy, peer-led team based learning, role play and scale up. This method of teaching was very interactive, attracts the student attention, retains the memory of the content and makes the student to search for books, journals and internet to find the solution to the questions.</p>
		
		
